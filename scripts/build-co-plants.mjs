/**
 * Reads CO SUN WIND OUTPUT.csv from the repo root and writes lib/co-energy-plants.ts
 * Run from repo root: node scripts/build-co-plants.mjs
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const csvPath = path.join(root, "CO SUN WIND OUTPUT.csv");
const outPath = path.join(root, "lib", "co-energy-plants.ts");

const raw = fs.readFileSync(csvPath, "utf8");
const lines = raw.split(/\r?\n/).filter((l) => l.trim().length > 0);

/** @type {{ lat: number; lng: number; kind: 'sun' | 'wnd' }[]} */
const plants = [];

for (let i = 0; i < lines.length; i++) {
  const line = lines[i];
  if (i === 0 && /^Plant_Code/i.test(line)) continue;

  const parts = line.split(",");
  if (parts.length < 4) continue;

  const lat = Number.parseFloat(parts[1]);
  const lng = Number.parseFloat(parts[2]);
  const src = parts[3].trim().toUpperCase();

  if (!Number.isFinite(lat) || !Number.isFinite(lng)) continue;

  let kind;
  if (src === "SUN") kind = "sun";
  else if (src === "WND") kind = "wnd";
  else continue;

  plants.push({ lat, lng, kind });
}

const header = `// Generated by scripts/build-co-plants.mjs from CO SUN WIND OUTPUT.csv — do not edit by hand.

export type CoEnergyPlantKind = "sun" | "wnd";

export type CoEnergyPlant = {
  lat: number;
  lng: number;
  kind: CoEnergyPlantKind;
};

`;

const body = `export const CO_ENERGY_PLANTS: readonly CoEnergyPlant[] = ${JSON.stringify(plants, null, 2)} as const;
`;

fs.writeFileSync(outPath, header + body, "utf8");
console.log(`Wrote ${plants.length} plants to lib/co-energy-plants.ts`);
